  <div class="green_tittle">Merchandise Uploading Policy</div>
  <div class="p_content">
  <p>
   	　&nbsp;&nbsp;In order to maintain the quality and integrity of the merchandise that is uploaded into www.dindin.com and providing more choices to the buyers, the merchants must adhere to its rules and regulations.<br>
　　Any violation on the rules during the uploading process of the merchandise may lead to the removal of the merchandise itself from the website, transaction will be blocked or even in some cases the merchant account will be blocked. If the website occurs any loses that was caused due to the merchant violating the regulations, DinDin has the right to pursue legal actions.<br>
　　一、Repeated uploading of merchandise<br>
　　二、Inconsistent merchandise prices<br>
　　三、Speculation and Promoting<br>
　　四、Advertisement<br>
　　五、Misplaced category<br>
　　六、Misleading use of keywords<br>
　　七、Inconsistent title, images, descriptions etc<br>
　　八、Illegal change of merchandise using cumulative sales<br>
　　九、Penalties<br>
</p>
 <p>
　　<span> 一、 Repeated uploading of merchandise</span><br>
　　Definition: A merchandise is only permitted to upload once, duplication are not allowed.<br>
　　Explanation: Important attributes mentioned in the definition is defined as:<br>
　　Color: Merchandise that comes with different color only needs one time upload.<br>
　　Size: Merchandise of the same model with different sizes only needs one time upload.<br>
　　Digital gadgets of various internal storage are prohibited from being uploaded with the same descriptions and images.<br>
　　Merchandise of the same model are prohibited from being uploaded multiple times with different free gifts or attachments.<br>
　　Multiple uploading of merchandise of the same model, by changing the price, time, quantity, combination and other forms, is deemed repeated uploading.<br>
　     Multiple uploading of service type merchandise with the same price and same service but different form is deemed repeated uploading.<br>
　     Other circumstances deemed repeated uploading by www.dindin.com.<br>
 </p>
 <p>
　　<span>二、Inconsistent merchandise prices</span><br>
　　Definition: Merchandise price or logistics fees inconsistent with the market demand or industry standard, abuse of web search to achieve high merchandise ranking, affecting the normal operation of www.dindin.com. <br>
　　Explanation: The product price and logistics fees are too high and does not match the market rate.<br>
　　Product selling price does not match the description<br>
　　	Upload free gifts with prices that are too high or too low (free gift and shop point redeemedmerchandise are not recommended for individual uploading, instead they should be included in the descriptions or title of other merchandise. If individual uploading is needed, it should be uploaded in the form of merchandise actual price or seller-borne postage fees);<br>
　　	Bulk or bundle-upmerchandise are not permitted to be uploaded individually with wholesale prices. It is recommended that they are uploaded with actual selling quantity. Wholesale prices can be included in the merchandise descriptions.<br>
 </p>
　<p><span>三、Speculation &amp; Promotion</span><br>
	Definition: Seller only advertise goods and speculate on the comment box.<br>
	Explanation: Upload only information but no merchandise to be sold<br>
	Obvious exchange of good comments in the merchandise message and description;　<br>
	Splitting a merchandise into multiple pages is not allowed　</p>　
 
　　<p><span>四、Advertisement</span><br>
　　Definition: Product descriptions incomplete, no actual merchandise and only provides contact method and product information/description not related (excluding residential and travel category).<br>
　　Explanation:Upload sold merchandise or merchandise only for viewing based on net price or auction;<br>
　　Publishown or others’ photos, photo or description of physical shop, brand story, industry knowledge or pure image (for viewing purpose of others);<br>
　　Publish merchandise based on wholesale, agent, advertisement, poster, merchandising and other forms; <br>
　　publish information related to free website registration;n Publish information that only includes the contact method or other non-sale merchandise information.<br></p>
 
　<p>　<span>五、Misplaced category</span><br>
　　Definition:Product does not tally with the selected category, or the merchandise is misplaced under www.dindin.com suggested category. <br>
　　Explanation:Product does not match with the published category;<br>
　　Occurrence of unrelated product on www.dindin.com suggested page.</p>
 
　<p><span>　六、Indiscriminate use of keywords</span><br>
　　Definition: The merchant intentionally uses words unrelated to the merchandise in order to attract more searches.<br>
　　Explanation:The merchant uses words unrelated to the product ,the merchant deliberately uses hot keywords in the merchandise name even though they are unrelated;<br>
　　The merchant uses brand names other than the brand of the product in the merchandise name;<br>
　　The merchant compares the merchandise with other brands in the merchandise name.<br></p>
　
 
　<p><span>七、Inconsistent title, images, and descriptions etc.</span>　<br>
　　Definition: The productdoes not match with the merchandise title, image, descriptions or other information.<br>
　　Explanation:The definition refers to merchandise name, image, descriptions, payment method and other information. Other than virtual goods and services, other merchandise should be published without images.<br>
　　Publishing of merchandise lacking the above elements (For example meaningless numbers and alphabets in the merchandise name or descriptions)<br>
　　Publishing of merchandise inconsistent with the above elements (For example, the merchandise name is 925 Sterling Silver Ring, but the merchandise image is a necklace etc.) <br>
　　Merchandise information which contains defamatory, abusive, pornographic, violent threats and other offensive language and other information not related to the merchandise (including, but not limited to, the following: disclose others’ ID without permission, chat history, trade disputes, uncivilized language without permission in the title or description of goods).<br></p>
 
　　<p><span>八、Illegal change of merchandise using cumulative sales</span><br>
　　Definition: The seller modifies merchandise information after accumulating sales, using one link to sell various merchandise.<br>
　　Explanation:	For example, the merchant uploads “*** shoes”. After sales have been accumulated, the merchant changes the merchandise image to “***cosmetics box”. This is a violation of our rules.<br>
 </p>
 
　<p>　<span>九、Penalties</span><br>
　　To establish a fair and competitive environment for merchants, improve the quality of goods, sales skills, and to provide a more efficient platform to buyers, www.dindin.com will take actions against users who violate the Merchandise UploadingManagement Rules, depending on the situation and severity. Violators will be required to pull their merchandise off the site, have their merchandise uploading limited, and shown warning.<br>
   	</p>
