  <div class="blue_tittle">报关流程</div>
  <p>
	  个人自用的海外邮寄物品按中国海关规定按入境个人邮递物品通关。入境个人邮递物品应以自用、合理数量为限，并如实进行申报。海关对入境个人邮递物品的管理原则是：既方便正常往来，照顾个人合理需要，又要限制走私违法活动。据此原则，海关规定了个人每次邮寄物品的限值、免税额和禁止、限制邮寄物品的品种。对邮寄入境的物品，海关依法进行查验，并按章征税或免税放行。因此要以合理自用数量为限，即自用指本人使用、馈赠亲友而非为出售或出租。合理数量指海关根据具体情况所确定的正常数量。通关流程如下图示：<br>
