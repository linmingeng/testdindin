	<p>
		<span>一、退换货基本原则</span><br>
		  1. 我们要求商家在发布其信息过程中，就物品本身要做到“售前信息充分披露”，即商家需要在物品信息自定义描述中将该类物品的具体状态、性质、新旧程度、污点/瑕疵、是否带礼包、是否含票、货品有效期、标签以及其他特殊情况阐述清楚。<br>
  		  2. 不论商家是否承诺可以退货，也不管其是否购买了运输保险，到货物品的损坏、丢失、缺货、错发，以及商家未尽到信息充分披露原则的情况都由商家承担责任，买家可以要求退款退货。<br>
   		 基于以上基本原则，商家可以根据实际情况设置以下两种不同的退换货规则。<br>
   		 	<span>（一）有条件退换货</span><br>
   		 	&nbsp;&nbsp; 该退货承诺是平台里最基本的退货条款。买家确认收货后，在明确非买家责任的前提下，买家可以提交退货请求。非买家责任一般指以下情况：<br>
    1.商家未尽到“售前信息充分披露”责任。物品信息中商家的自定义内容中没有披露导致买家退货的问题信息。<br>
    2.其他非买家导致的诸如破损、质量问题、错发品种、错误尺寸和颜色、有效期失效、完全不符合物品信息的中商家的自定义描述。<br>
    特别提醒：产品过敏性问题不属于质量问题。<br>
    有条件退换货具体规则如下：<br>
    1.买家收到包裹时有责任在签收前检查物品完整性和数量的一致性，若遇物品受损、丢失、数量不够、物品不相符的情况，请务必拒签。同时要求商家退回相应的货款、服务费、所产生的运费以及其他可能已经支付的费用。<br>
    2.若在签收后发现物品损坏、丢失、缺货、错发等情况，买家需要尽快同商家进行协商解决。只有在买家充分举证确认非用户责任的前提下，买家才可以要求商家退回相应的货款和服务费。<br>
    特别提醒：强烈建议买家在签收前，当着快递人员面仔细检查到货情况，如有问题，请务必拒签要求退回，或者当场拍照取证并获得快递人员签字证明。<br>
    取证范围包括但不限于：<br>
1)	包裹外表，快递单号，该商家在包装上的独特标识等。<br>
2)	打开包裹时刻的状态，包装状态，相应数量。<br>
3)	有损坏物品细节。<br>
4)	取证日期。<br>
    
建议商家<br>
1)在发送包裹前，购买运输保险。<br>
2)在包裹外包装上加贴独特防盗封膜。<br>
3)在所发出物品上加封其独特标签。<br>

	<span>（二）可以退货</span><br>
&nbsp;&nbsp;“可以退货”规则是指在承诺“有条件退货”的基础之上，商家承诺买家可以在确认收货７天之内因为种种自身原因而提出退货。买家自身原因包括但不限于后悔购买、个人喜好、尺寸不合适等任何其他原因。
    因为买家自身原因提出的退货，退货的物品状态要求满足以下基本条件：<br>
1.	不影响商家二次销售。未开封，未使用，包装完好，物品状态良好，<br>
2.	所附带的所有附件、礼品、收据、商家的独特辨识码都保持完好无损。<br>
3.	商家在自定义描述中提出的其他条件。<br>
    由于自身原因发生的退货，买家可以要求商家退回相应物品的货款和服务费。但商家有权将已经发生的运费、关税等保留不退。<br>
   <span>（三）其他未尽事宜</span> <br>
1. 原则上买家提出的退货申请，需要尽其自身的举证责任，平台会协助买家进行举证。<br>
2. 在不违反基本用户协议，不人身攻击、恶意攻击或者不恶意诬蔑情况下，买家有自由发表评论、评价和投诉的权利，这与提出的退货申请是否得到满足无关。<br></p>
<p><span>二、退换货细则</span>	<br>
平台上的所有商家承诺，当签收货物后7天内，因产品质量问题或包裹破损，以及对商品不满意、不喜欢、在不影响二次销售的情况下，都可以向平台提出退换货申请。<br>
<span>特别提醒：为了保障所有买家的健康安全，离开仓储监管的食品、日化品均不会再次销售，因此所有奶粉、辅食、零食、特产、保健品等食品、营养品类，以及贴身使用类的日用品、化妆品均不在退换货范畴内。请买家在购买以上货物时予以配合，还望各位买家理解。</span>

