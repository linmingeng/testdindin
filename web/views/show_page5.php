	<span>（二）海外直发件退换货特别说明</span><br>
直邮海外直发商品在没有品质、错发等问题的情况下，是不予退换的。<br>
国际直邮海外直发，如奶粉类，出现瘪罐、爆罐（奶粉漏出）情况，会根据实际情况作出损坏单位10%-20%的补偿。由于国际直邮时间会比较长，因海关原因造成的延误如海关清关量比较大、商品滞留海关等情况，均不构成退换货的条件。<br>
	<span>（三）收货及退换货原则</span><br>
1.	为了保障用户的权益，在收货时请和物流、快递公司递送人员一起拆开包裹，并当面清点货品，认真验货。如发现货品、数量、品质与发货单不符或其他问题，以致影响产品正常使用，请买家及时反馈给递送人员并当面拒收，拍照或有递送人员签字确认，放可作为退换货申请理由。在收到包裹时，如发现包裹破损，请买家要求物流、快递公司出具包裹破损证明。<br>
2.	自收到货品七日内（以签收日为准）出现质量问题的（在正常使用过程中），买家事先需通过电话或者E-mail的方式与平台客户服务人员取得联系，告知订单号及退换货原因，将商品、收货单用快递方式寄给平台公司地址，待平台售后专业人员验收后，确认符合退换货条件，我们会尽快办理退换货。<br>
3.	因商品质量问题退换货所产生的邮寄（普通快递包裹）费用，由卖家承担。买家需将邮寄费用的复印件连同商品、发货单一同寄回平台，我们会将邮寄费通过买家最方便使用的付款方式返还给买家。<br>
4.	由于买家自身原因退货、换等值或超值的商品时（含没选好尺码、颜色、收到后改变主意等），由买家承担本次订单发货和退货所需的物流或快递费用（非卖家原因退款的情形，买家需承担本次订单交易支付产生的4%交易手续费用）；换货的情况下，还需要承担再次发货时的运费。<br>
5.	对于食品及保健品类商品不在退换货范围内，请您服用前认真阅读使用说明书，禁忌人群切勿服用。<br>
6.	对于日用品、化妆品类商品不在退换货范围内，购买前务必确认是否会产生过敏或不良反应；请您使用前认真阅读使用说明书，或使用前做一个简单的皮肤过敏测试以确保安全使用。<br>
7.	应退货款采用与该笔支付方式对应的方式退还，所有退款均不会以现金形式退款。<br>
8.	请买家务必当面对照发货单核对商品，如果出现商品数量缺少、商品破损，请您当场办理整单商品的退货。若订单中含有赠品，请一并退回。货单一旦确认签字，我们将无法办理退换或补发。<br>
9.	如果是成套商品，买家必须整套退货。如果是捆绑商品，买家在退回商品的同时必须将赠品一并退回。<br>
	<span>（四）退换货货品标准</span><br>
出现下列任何一种情况均不提供退换货：<br>
1.	产品曾受到非正常使用；<br>
2.	非正常条件下存储、暴露在潮湿环境中、暴露在温度过高或过低环境中；<br>
3.	未经授权的修理、误用、疏忽、滥用、事故、改动；<br>
4.	不正确的安装、不可抗力、食物或液体溅落、产品的正常磨损等；<br>
5.	在退换货之前未与平台客户服务人员取得联系；<br>
6.	所有食品类、保健品、日化用品商品，均不在退换货范畴；<br>
7.	超出退换货期限（七日内可退换货；七至十五日内可换货）；<br>
8.	退回的商品外包装不完整、所附配件及资料、赠品不齐全的；<br>
9.	发货单丢失、涂改或损坏；<br>
10.	直邮类海外商品，均符合当地国检验检疫标准，买家一旦购买，即视为接受国际标准。<br>
	 <span>（五）不符合退换货情形处理</span><br>
买家要求退换货的产品寄送至平台公司地址，经平台专业售后人员检测本产品并未符合退换货的情形的处理方案：<br>
1.	不予退换，买家放弃该产品，由平台处理。<br>
2.	买家承担复寄返回的费用，将产品寄回给买家。<br>
3.	协商退还50%货款，平台作为线下处理。<br>
