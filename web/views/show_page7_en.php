 	<h5>1.	Register</h5>
 	<p>1)、First step, please register to be a member before you are a merchant. Go to the website : http://www.dindin.com, Click red rectangle part.<br>
<img src="../image/image002.jpg">
</p>
<p>2)、Then jump to the following page.<br>
<img src="../image/image003.jpg">

</p>
<p>3)、Choose QQ or mail address, write down your QQ number or mail address, and put in the password and verify code, then click to be a member.
（PS: Overseas member can’t use phone number to register）.<br>
<img src="../image/image004.jpg">
</p>
<h5>2、Become Merchant</h5>
<p>1)、When you see this page, click the second green button to apply to be a merchant.<br>
<img src="../image/image005.jpg">
</p>
<p>2)、In this page, please enter the real company info. in following part:
Company name   Company address    Postal code   Contact Name
Contact  Number   QQ  Number  Business License(if you don’t have, please enter your ID number)  Company Introduction   At last  click and confirm.
<br>
<img src="../image/image006.jpg">
</p>
<p>3)、If you see this page, congratulations! You already become a merchant in Dindin, we will verify your info. in our backstage and pass it. When we finished the audit, please click the red circle part.<br>
<img src="../image/image007.jpg">
</p>
<h5>3、Login interface</h5>
<p>1)、Find out “ My Dindin “, click the last item.<br>
<br>
<img src="../image/image008.jpg">
</p>
<p>2)、The user name and password in the login page, the user name is your Dindin number or register email.<br>
<img src="../image/image009.jpg" style="height: 429px; width: 625px">
</p>
<p>3)、Enter the merchant management backstage.<br>

<img src="../image/image010.jpg">
</p>
<h5>4、upload</h5>
<p>
1)、Publish new products: click publish new products to upload the Commodity to enter into the business center, enter into publish new products.<br>
<img src="../image/image011.jpg"><br>
</p>
<p>2)、Enter the new Commodity page.<br>
<img src="../image/image012.jpg">
</p>
<p>3)、Click the commodity information.<br>
<img src="../image/image013.jpg">
</p>
<p>4)、Product name: Pleas follow the model to set(Brand name + Sample + Product name + Detail ).<br>
<img src="../image/image014.jpg">
</p>
<p>5)、Commodity model: You can choose to set it or not.<br>
<img src="../image/image015.jpg">
</p>
<p>6)、Product category: According to your products category to choose it, please choose to the most detail category.<br>
<img src="../image/image016.jpg"><br>
<img src="../image/image017.jpg"><br>
<img src="../image/image018.jpg">
</p>
<p>7)、Commodity properties : According to your products detail, if you wanna open some of the attribute specification, you can send the detail requirements to merchants@dindin.com.<br>
<img src="../image/image019.jpg">
</p>
<p>8)、Based on the products actually inventory to enter.<br>
<img src="../image/image020.jpg">
</p>
<p>9)、The unit will be  个， 盒，件，支，瓶 ect.<br>
<img src="../image/image021.jpg">
</p>
<p>10)、Upload photos.<br>
<img src="../image/image022.jpg">
</p>
<p>11)、Commodity picture: At lease upload one of the product’s clearly picture, the size will be 300×300, less than 1M, it will show on the top of the product webpage.<br>
