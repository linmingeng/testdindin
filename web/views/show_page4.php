     <p><span>3、物品质量问题的判断规则</span><br>
     	&nbsp;&nbsp; 1） “物品描述”规则<br>
   &nbsp;&nbsp; “物品描述”应对物品外观、成份、含量、质量、成新率、包装、保修、保质期、售后服务、产地、厂商名称等销售时必备的物品要素进行说明。<br>
   &nbsp;&nbsp; 如“物品描述”中未对上述事项部分或全部进行明确说明，则应以同类产品的国家标准、行业标准或厂商标准为准，或视为双方对此无争议；<br>
  &nbsp;&nbsp;  如物品存在瑕疵，应在“物品描述”进行完整瑕疵描述，没有进行瑕疵描述的物品视为无瑕疵物品；<br>
   &nbsp;&nbsp; 如双方通过其他方式就“物品描述”的修改达成一致的，应以双方修改后的描述为准，异议方必须提供“足够且确切”的证据来证明； <br>
   &nbsp;&nbsp; 类似“详细描述见http://******”的说明是无效说明，并不能以指向其他网站物品说明取代叮叮网的“物品描述”；<br>
   &nbsp;&nbsp; 买家收到之物品虽与“物品描述”不一致，但是双方事后协商一致无异议的，视为无异议；<br>
   &nbsp;&nbsp; 对于物品外观、成份、含量、质量、包装、保修、成新率、保质期、售后服务、产地、厂商名称等说明，应以成交日叮叮网网上所发布的“物品描述”为准。<br>
  &nbsp;&nbsp;  2) 商家应对出售的物品描述负有证明责任，可以对物品的说明提供包括但不限于厂家的进货凭证，产品合格证，正规的商业发票等证明文件。<br>
  &nbsp;&nbsp;  3）收货方收到的物品质量有问题且从外观上无法判断的，应当出具质量监督管理局的检测证明或相应品牌维修中心针对物品如实地进行检测或鉴定后出具的带有公章的书面检测凭证。<br>
   &nbsp;&nbsp; 4）因交易双方约定不清而产生交易纠纷的，撤销该交易，因此导致的损失由交易双方共同承担，具体承担比例由叮叮网根据具体情况判断。<br>
   &nbsp;&nbsp; 5)二手物品<br>
  &nbsp;&nbsp;  功能缺陷：可参照物品描述规则并提供检测证明，客观上无法提供检测证明或者提供检测证明的代价超过争议金额本身而导致事实无法查明的，按照前款规定进行处理。
    &nbsp;&nbsp;叮叮网用户的行为，发生在本规则生效或修订以前的，适用当时的规则；发生在本规则生效或修订以后的，适用本规则。<br>
   &nbsp;&nbsp; 以上所有条款最终解释权归叮叮网所有。<br>
     	
