	   <font style="font-size:15px;font-weight:bold;">注册流程</font></p>
	   <h5>1.	注册会员</h5>
 	<p>1)、首先，在成为商家之前请注册成为会员，进入到以下网址(www.dindin.com)，点击红色框部分<br>
<img src="../image/image002.jpg">
</p>
<p>2)、然后跳到下一页面<br>
<img src="../image/image003.jpg">

</p>
<p>3)、选择点击QQ或邮箱，写下QQ号码邮箱地址，输入密码以及验证码，然后点击成为会员。<br>
<img src="../image/image004.jpg">
</p>
<h5>2、成为商家</h5>
<p>1)、当你看到这个页面，点击第二个绿色的按钮申请成为商家。<br>
<img src="../image/image005.jpg">
</p>
<p>2)、在这个页面，输入真实的企业信息到以下部分<br>
<img src="../image/image006.jpg">
</p>
<p>3)、如果你看到这个页面，恭喜！你是商家了，我们会在后台审核并通过。通过后，请点击红色圈部分。<br>
<img src="../image/image007.jpg">
</p>
<h5>3、登录界面</h5>
<p>1)、找到“ My Dindin ”，点击商家中心。<br>
<br>
<img src="../image/image008.jpg">
</p>
<p>2)、在登录页面中的商家中心输入用户名和密码，用户名为注册叮叮号或者注册邮箱。<br>
<img src="../image/image009.jpg" style="height: 429px; width: 625px">
</p>
<p>3)、登录成功后，进入商家管理后台。<br>

<img src="../image/image010.jpg">
</p>
<h5>4、发布产品</h5>
<p>
1)、发布新产品：在点击发布新产品可以上传商品，进入商家中心后，点击发布新商品。<br>
<img src="../image/image011.jpg"><br>
</p>
<p>2)、进入发布新商品页面。<br>
<img src="../image/image012.jpg">
</p>
<p>3)、开始填写商品信息。<br>
<img src="../image/image013.jpg">
</p>
<p>4)、商品名称：请按照模板进行设置名称（品牌+样式+物品名+详情：eg:Gucci蓝色小手包100%美国正品）。<br>
<img src="../image/image014.jpg">
</p>
<p>5)、商品型号：可设置可不设置。<br>
<img src="../image/image015.jpg">
</p>
<p>6)、商品类别：请根据实际商品类别进行选择,需选择至最细分类别。<br>
<img src="../image/image016.jpg"><br>
<img src="../image/image017.jpg"><br>
<img src="../image/image018.jpg">
</p>
<p>7)、商品属性：根据您的产品详情，如产品有需要开启相关属性规格可将具体需求发送至 merchants@dindin.com。<br>
<img src="../image/image019.jpg">
</p>
<p>8)、发布数量：根据产品实际库存填写。<br>
<img src="../image/image020.jpg">
</p>
<p>9)、单位为个， 盒，件，支，瓶等等。<br>
<img src="../image/image021.jpg">
</p>
<p>10)、上传图片。<br>
<img src="../image/image022.jpg">
</p>
<p>11)、商品图片：至少上传一张商品清晰图片，图片规格为300×300，大小少于1M，该图片将显示在产品详情页的。<br>
<img src="../image/image023.jpg">
</p>
<p>12)、商品详情：请按照模板进行填写。<br>
