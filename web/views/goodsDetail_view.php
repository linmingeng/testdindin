// 延时关闭
function delayHide( selector ) {
  clearTimeout( delayHide._timer );
  delayHide._timer = setTimeout( function() {
    $( selector ).hide();
  } , 200 );
}
// 取消延时关闭
function cancelDelayHide() {
  clearTimeout( delayHide._timer );
}

// li:mouseover
function show() {
  cancelDelayHide();
  $( '.ratings' ).show();
}
// li:mouseout
function hide() {
  delayHide( '.ratings' );
}
